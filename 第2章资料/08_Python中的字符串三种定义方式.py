"""
字符串的三种定义方法：
- 单引号定义法
- 双引号定义法
- 三引号定义法
"""

# 单引号定义法
name = '单引号定义法'
print(name)

# 双引号定义法
name = "双引号定义法"
print(name)

# 三引号定义法
name = """三引号定义法"""
print(name)

# 若定义的字符串本身含有单引号双引号
# 单引号定义法内含双引号
name = '"双引号"'
print(name)

# 双引号定义法内含单引号
name = "'单引号'"
print(name)

# 转义字符(\)解除引号解释效用，变为普通字符串
name = '\'转义字符解除单引号\''
print(name)
name = "\"转义字符解除双引号\""
print(name)
